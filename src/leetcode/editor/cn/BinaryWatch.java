          public List<String> getLeftOut(String[] arr1,String[] arr2) {
              Set<String> set = new HashSet<>();
              for (int i = 0; i < arr2.length; i++) {
                  set.add(arr2[i]);
              }
              List<String> list = new ArrayList<>();
              for (int i = 0; i < arr1.length; i++) {
                  if (!set.contains(arr1[i])) {
                      list.add(arr1[i]);
                  }
              }
              return list;
          }
